<h2>Minecraft (2011)</h2>
<img src="minecraft.jpg" alt="Mijn Foto" width="300">

<article>
De populairste game van het decennium is er een die alle vooroordelen over computerspellen onderuithaalt. <br>
Ouders kunnen hun kind gerust urenlang voor de pc zetten om Minecraft te spelen, daar leert-ie alleen maar van. <br>
In plaats van gewelddadig, dom of verslavend, is Minecraft een speeltuin voor de creatieve geest: met de bekende blokken bouw je huizen, hele steden, werelden of zelfs werkende computers. 
<br>Het simpele concept sloeg aan. Al vóór de officiële release in 2011 was Minecraft een enorme hype: gamers begonnen naar lieverlee de levels te delen die ze hadden gebouwd.
<br> Er verschenen talloze mods, bijvoorbeeld om de kenmerkende hotseknots-graphics op te fleuren.
Minecraft werd zo’n fenomeen dat scholen en musea de game als educatieve tool begonnen te gebruiken. <br>
Eigenlijk is het een wonder dat het nog tot 2014 duurde tot Microsoft het bedrijfje achter de game voor 2,5 miljard dollar opkocht. <br>
Minecraft is een goudmijn die nog lang niet is uitgeput. Het is de Lego van deze generatie.
</article>
