<h2>The Last of Us (2013)</h2>

<img src="last.jpg" alt="Mijn Foto" width="300">
<article>

Zombies, de post-apocalyps, extreem geweld – het was al zo vaak gedaan. <br>
Toch verraste The Last of Us, van het hartverscheurende begin tot het inmiddels legendarische einde. <br>
De game vertelt over een man en een meisje. Hij is zijn dochter verloren, zij is waarschijnlijk als enige immuun voor het zombievirus waaraan bijna de hele mensheid verloren is gegaan. 
<br>Samen reizen ze door een Amerika dat is heroverd door de natuur: kantoorgebouwen zijn overwoekerd door groen en giraffen lopen door de straten.
<br>
Het is een klein, simpel verhaal, dat Naughty Dog als startpunt gebruikt om dingen te zeggen over ouderschap, verlies en de aard van de mens <br>.
Waar de gamestudio met Uncharted een vrolijk beeld van de wereld schetst, houdt het in The Last of Us de mensheid een spiegel voor. <br>
Het is geen fraai beeld, maar levert wel een prachtige game op. <br>
 Eén die ook knap de val ontwijkt waar Uncharted keer op keer in trapte: spel en verhaal staan niet los van elkaar, maar versterken elkaar, <br>
  vooral op de helse Grounded-moeilijkheidsgraad. Daarmee is The Last of Us de beste verhalende game van de jaren tien.
</article>

<h2>The Legend of Zelda: Breath of the Wild (2017)</h2>
<img src="zelda.jpg" alt="Mijn Foto" width="300">
<article>In een decennium gedomineerd door openwereldgames was Breath of the Wild de beste openwereldgame. <br>
In een decennium waar Nintendo het spoor bijster leek. <br>
De Wii sprak vooral gezinnen aan, de Wii U was keihard geflopt – gaf Breath of the Wild, als lanceringsgame voor de Switch,<br>
het startschot voor de wederopstanding van het bedrijf. <br>
 De game liet zien: Nintendo heeft het nog steeds, loopt nog steeds voorop – <br> en weet als geen ander jong, oud, hardcore en casual tegelijk te bedienen.

Breath of the Wild fikste alles wat mis is met moderne openwereldgames. Waar veel games voelen als werk, wist Nintendo craften, sidequests en routineus torens beklimmen leuk te maken. <br>
In Hyrule was je reis de bestemming: alleen al van A naar B zien te komen was goed voor vermaak, niet in de laatste plaats doordat je praktisch overal op kon klimmen. <br>

En de grootste verdienste: Breath of the Wild loste de belofte van de allereerste Zelda in. <br>
 Jij, je fantasie, en de absolute vrijheid om in een wereld zonder grenzen jouw eigen avontuur te beleven. <br>
  Het is niet alleen het summum van het gamedecennium, maar een van de allerbeste games ooit gemaakt.</article>
    


